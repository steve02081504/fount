import axios from 'npm:axios'

/**
 * 用于与 Blackbox.ai API 交互的 BlackboxAI 类。
 */
export class BlackboxAI {
	/**
	 * 创建 BlackboxAI 的实例。
	 * @param {object} config - The configuration object.
	 */
	constructor(config) {
		this.apiUrl = 'https://api.blackbox.ai/api/chat'
		this.headers = {
			'Content-Type': 'application/json',
		}
		this.config = config
		this.conversationHistory = {}
		this.defaultModel = 'blackboxai'
	}

	/**
	 * 向 Blackbox.ai API 发送请求。
	 * @param {Array<object>} messages - The array of message objects.
	 * @param {string} model - The model to use for the request.
	 * @returns {Promise<string>} The response from the API.
	 */
	async call(messages, model) {
		const payload = {
			messages,
			id: Date.now().toString(),
			previewToken: null,
			userId: null,

			codeModelMode: true,
			agentMode: {},
			trendingAgentMode: {},
			isMicMode: false,
			userSystemPrompt: null,
			maxTokens: 1024,
			playgroundTopP: 0.9,
			playgroundTemperature: 0.5,
			isChromeExt: false,
			githubToken: null,
			clickedAnswer2: false,
			clickedAnswer3: false,
			clickedForceWebSearch: false,
			visitFromDelta: false,
			mobileClient: false,
			userSelectedModel: model || this.defaultModel
		}

		try {
			const response = await axios.post(this.apiUrl, payload, { headers: this.headers })
			return response.data.replace(/\n*generated by blackbox\.ai, try unlimited chat https:\/\/www\.blackbox\.ai\/?\n*/gi, '')
		}
		catch (error) {
			console.error('Error communicating with Blackbox.ai:', error)
			throw error
		}
	}

	/**
	 * 计算给定文本中的令牌数。
	 * @param {string} text - The text to count tokens for.
	 * @returns {Promise<number>} The number of tokens.
	 */
	async countTokens(text) {
		const payload = {
			text
		}

		try {
			const response = await axios.post('https://api.blackbox.ai/api/token-count', payload, { headers: this.headers })
			return response.data.tokenCount
		}
		catch (error) {
			console.error('Error communicating with Blackbox.ai:', error)
			throw error
		}
	}
}
