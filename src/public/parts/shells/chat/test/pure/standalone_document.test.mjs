/**
 * standaloneDocument：完整离线 HTML 包装（OG / DaisyUI / github-markdown-css / 附件）。
 */
/* global Deno */
import { assert, assertStringIncludes } from 'https://deno.land/std@0.224.0/assert/mod.ts'

import { installMarkdownTestDom } from './markdown_test_dom.mjs'

installMarkdownTestDom()

const { wrapStandaloneMarkdownDocument } = await import('../../../../../pages/scripts/features/markdown/standaloneDocument.mjs')

Deno.test('wrapStandaloneMarkdownDocument emits full document shell', () => {
	const html = wrapStandaloneMarkdownDocument('<h1>Hello</h1><p>world body</p>', {
		locale: 'zh-CN',
		downloadLabel: '下载',
	})
	assertStringIncludes(html, '<!DOCTYPE html>')
	assertStringIncludes(html, 'lang="zh-CN"')
	assertStringIncludes(html, '<title>Hello</title>')
	assertStringIncludes(html, 'og:description')
	assertStringIncludes(html, 'github-markdown-')
	assertStringIncludes(html, 'daisyui')
	assertStringIncludes(html, 'class="flex-grow markdown-body"')
	assertStringIncludes(html, '<h1>Hello</h1>')
	assertStringIncludes(html, 'Generated by')
	assertStringIncludes(html, 'https://github.com/steve02081504/fount')
})

Deno.test('wrapStandaloneMarkdownDocument uses first heading of any level as title', () => {
	const html = wrapStandaloneMarkdownDocument(
		'<h2>🎯 ZL-31 多格式渲染压力测试</h2><p>这是一段包含粗体的正文，用于验证标题提取。</p>',
	)
	assertStringIncludes(html, '<title>🎯 ZL-31 多格式渲染压力测试</title>')
	assert(!html.includes('<title>🎯 ZL-31 多格式渲染压力测试 这是一段'), 'title must not spill into body text')
})

Deno.test('wrapStandaloneMarkdownDocument embeds image attachment + modal', () => {
	const html = wrapStandaloneMarkdownDocument('<p>pic</p>', {
		files: [{
			name: 'a.png',
			mime_type: 'image/png',
			buffer: 'AAAA',
		}],
		downloadLabel: 'Download',
	})
	assertStringIncludes(html, 'data:image/png;base64,AAAA')
	assertStringIncludes(html, 'function openModal')
	assertStringIncludes(html, 'class="attachments"')
	assertStringIncludes(html, 'download="a.png"')
})

Deno.test('wrapStandaloneMarkdownDocument adds katex css when needed', () => {
	const html = wrapStandaloneMarkdownDocument('<span class="katex-mathml">x</span>')
	assertStringIncludes(html, 'katex.min.css')
})
